# 위계적 회귀분석


# 변수의 척도가 등간척도와 비율척도로 이루어져 있는 경우에 사용
#  다중회귀분석 : 2개 이상의 독립변수를 모두 투입하여 연구모형을 분석
#  위계적 회귀분석 : 연구자의 경험적 근거를 바탕으로 영향력이 큰 변수를 하나씩 투입해가면서 독립변수들 가운데
# 가장 영향력이 큰 변수는 무엇이고, 가장 작은 영향력을 미치는 변수는 무엇인지 확인 가능


# 스마트폰의 사용자가 만족을 느끼는 요인이 외관, 유용성, 편의성 중 어떤 것인지 알아보기 위해
# 위계적 회귀분석을 진행하였다. 4가지 변수에 대해 총 15문항으로 설문을 실시했다.
# 어떠한 변수가 가장 큰 영향을 미치는지 알아보자. (data > 실습파일 > 6.위계적 회귀분석.sav)

rm(list=ls())


library(dplyr)
library(haven)
library(psych)



setwd("~/R/Regression/SPSS_다변량회귀분석/01.상반부 자료/실습자료2")

df <- read_spss("6. 위계적 회귀분석.sav")
df <- mutate(df, 외관 = (외관1+외관2+외관3)/3, 편의성 = (편의성1+편의성2+편의성3+편의성4)/4,
               유용성 = (유용성1+유용성2+유용성3+유용성4+유용성5)/5, 만족감 = (만족감1+만족감2+만족감3)/3)
df.a <- df[,c(16:19)]

describe(df.a)

# 변수 투입방법
# 1단계 : 외관, 2단계 : 외관 + 편의성, 3단계 : 외관 + 편의성 + 유용성 순으로 투입 예정


df.m1 <- lm(만족감~외관,data = df.a)
df.m2 <- lm(만족감~외관+편의성,data = df.a)
df.m3 <- lm(만족감~외관+편의성+유용성,data = df.a)

summary(df.m1)
summary(df.m2)
summary(df.m3)

anova(df.m1)
anova(df.m2)
anova(df.m3)
anova(df.m1, df.m2, df.m3)

# [모양 요약] : 모형 1, 모형 2, 모형 3에 따라 독립변수가 투입되며, R 제곱값으로
# 회귀식의 설명력을 확인. Durbin-Watson의 값이 1.831로 2에 근접 독립적
# 모형 1(df.m1): 독립변수는 ‘외관’으로, 종속변수와의 상관관계는 R=.325
# 종속변수에 대한 독립변수의 설명력은 10.5%

# 모형 2(df.m2): 독립변수는 ‘외관, 편의성’으로, 종속변수와 상관관계는 R=.450
# 종속변수에 대한 독립변수의 설명력은 20.2%

# 모형 3(df.m3): 독립변수는 ‘외관, 편의성, 유용성’으로 종속변수와의 상관관계는
# R=.487, 종속변수에 대한 독립변수의 설명력은 23.7%

# R값은 종속변수와 입력변수간의 상관계수로서, 간단하게 R값을 구하고 싶으면 sqrt(R² 점수)를 사용하면 된다.

