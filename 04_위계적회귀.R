# [분산분석]: F값 유의확률을 확인 ▶ 목적에 맞는 회귀식 여부 판단
#  모형 1: F=38.053, 유의확률 .000 ▶ 통계적 유의
#  모형 2: F=40.789, 유의확률 .000 ▶ 통계적 유의
