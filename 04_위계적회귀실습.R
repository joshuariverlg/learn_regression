str(df.a)
describe(df.a)

# 변수 투입방법
# 1단계 : 목표지향성, 2단계 : 목표지향성 + 활동지향성, 3단계 : 목표지향성 + 활동지향성 + 학습지향성 순으로 투입 예정


df.m1 <- lm(참여지속의도~목표지향성,data = df.a)
df.m2 <- lm(참여지속의도~목표지향성+활동지향성,data = df.a)
df.m3 <- lm(참여지속의도~목표지향성+활동지향성+학습지향성,data = df.a)

summary(df.m1)
summary(df.m2)
summary(df.m3)

anova(df.m1)
anova(df.m2)
anova(df.m3)
anova(df.m1, df.m2, df.m3)

# [모양 요약] : 모형 1, 모형 2, 모형 3에 따라 독립변수가 투입되며, R 제곱값으로
