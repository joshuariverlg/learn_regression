# 종속변수에 대한 독립변수의 설명력은 26.1%

# 모형 2(df.m2): 독립변수는 ‘목표지향성, 활동지향성’으로, 종속변수와 상관관계는 R=.5242137
# 종속변수에 대한 독립변수의 설명력은 27.48%

# 모형 3(df.m3): 독립변수는 ‘목표지향성, 활동지향성, 학습지향성’으로 종속변수와의 상관관계는
# R=.6302, 종속변수에 대한 독립변수의 설명력은 39.7%

# R값은 종속변수와 입력변수간의 상관계수로서, 간단하게 R값을 구하고 싶으면 sqrt(R² 점수)를 사용하면 된다.

